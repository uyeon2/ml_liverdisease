def liver_feedback(probability):
	if probability >= 0.8:
		return (
			"🚨 간 질환 위험이 매우 높습니다.",
			[
				"1개월 이내에 병원 방문하여 간 기능 검사(AST, ALT, GGT 등) 및 초음파 검사를 받는 것을 강력히 권장합니다.",
				"절대 금주 및 금연을 실천하세요.",
				"지방간/간염 가능성이 높으므로, 고단백 저지방 식단 (예: 생선, 콩, 채소) 위주로 섭취하고 포화지방과 당류는 피하십시오.",
				"체중 감량이 필요한 경우, 의사와 상담 후 주당 0.5~1kg씩 점진적으로 감량하세요."
			]
		)

	elif probability >= 0.5:
		return (
			"⚠️ 간 질환 위험이 다소 높은 편입니다.",
			[
				"가까운 내과에서 기본 혈액검사(LFT)를 받아보는 것을 추천합니다.",
				"주 3회 이상 30분 걷기 운동을 시작하세요. 이는 간 대사와 인슐린 저항성 개선에 도움을 줍니다.",
				"알코올 섭취는 주 2회, 1~2잔 이내로 제한하세요.",
				"고염식, 가공식품, 트랜스지방 섭취를 줄이고 수분 섭취를 늘리세요."
			]
		)

	elif probability >= 0.2:
		return (
			"🌱 간 질환 위험이 낮은 편입니다.",
			[
				"현재 건강을 정기검진으로 점검(1년에 1회) 유지하세요.",
				"운동을 꾸준히 유지하되, 근력운동과 유산소 운동을 병행하면 더욱 좋습니다.",
				"가공식품과 술은 주 1~2회 이하로 제한하며, 야식은 자제하세요.",
				"물을 하루 1.5~2L 섭취하고, 수면시간(7시간 이상)을 규칙적으로 유지하세요."
			]
		)

	else:
		return (
			"🎉 간 질환 위험이 매우 낮습니다.",
			[
				"건강한 생활습관을 잘 유지하고 계십니다.",
				"음주, 흡연 없이 균형 잡힌 식사를 하고 있다면 그대로 유지하세요.",
				"간 건강에 좋은 식품: 커피(하루 1~2잔), 채소, 해조류, 견과류",
				"스트레스 관리(명상, 산책, 취미활동 등)도 간 건강에 긍정적인 영향을 줍니다."
			]
		)
